import React from 'react';

class Link extends React.Component {
  constructor() {
    super();
  }

  handleClick(event) {
    event.preventDefault();
  };

  render() {
    const { to, children } = this.props;
    return (
      <a href={to} onClick={this.handleClick}>
        {children}
      </a>
    );
  }
}

export default Link;
